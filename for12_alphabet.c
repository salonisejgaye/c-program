//wap to print alphabet series
#include<stdio.h>
void main()
{
    int i;
    for(i=65;i<=90;i++)
    {
        printf("%c",i);
    }     
    printf("\n________________________________\n");
    for(i='A';i<='Z';i++)
    {
        printf("%c",i);
    }     
    printf("\n________________________________\n");
    for(i=97;i<=122;i++)
    {
        printf("%c",i);
    }     
    printf("\n________________________________\n");
    for(i='a';i<='z';i++)
    {
        printf("%c",i);
    }     
    
}